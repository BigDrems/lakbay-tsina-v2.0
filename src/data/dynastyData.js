// Timeline configuration
export const timelineConfig = {
  startYear: -2100, // Approximate start of Xia Dynasty
  endYear: 1912, // End of Qing Dynasty
  getPosition: (yearStr) => {
    // Parse year from string like "c. 2070–1600 BCE" or "1644–1912 CE"
    const parseYear = (str) => {
      const startMatch = str.match(/(\d+)/);
      if (startMatch) {
        const year = parseInt(startMatch[1]);
        return str.includes("BCE") ? -year : year;
      }
      return 0;
    };

    const startYear = parseYear(yearStr);
    return (
      ((startYear - timelineConfig.startYear) /
        (timelineConfig.endYear - timelineConfig.startYear)) *
      100
    );
  },
};
